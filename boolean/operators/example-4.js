//don't confuse equality and strict inequality 
// with strict equality and strict inequality

// Equality (==) and Inequality(!=)
console.log('1' == 1);
// expect output: true

console.log('1' != 1);
// expect output: false

console.log(null == undefined);
// expect output: true 

// Strict equality(===) and Strict inequality (!==)
console.log('1' === 1);
// expect output: false

console.log('1' !== 1);
// expect output: true

console.log(null === undefined);
// expect output: false