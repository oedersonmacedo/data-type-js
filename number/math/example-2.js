console.log(Math.abs(10));
// expect output: 10

console.log(Math.abs(-10));
// expect output: 10

console.log(Math.ceil(1.1));
// expect output: 2

console.log(Math.ceil(-1.1));
// expect output: -1

console.log(Math.floor(9.9));
// expect output: 9

console.log(Math.floor(-9.9));
// expect output: -10

console.log(Math.round(4.5));
// expect output: -5

console.log(Math.round(-4.5));
// expect output: -4

console.log(Math.sign(5));
// expect output: 1

console.log(Math.sign(-5));
// expect output: -1

console.log(Math.trunc(2.3));
// expect output: 2

console.log(Math.trunc(-2.3));
// expect output: -2