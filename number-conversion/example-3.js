console.log((0xA).toString(10)); // Convert for Decimal
// expect output: '10'

console.log((0b1010).toString(16)); // Convert for Hexadecimal
// expect output: 'a'

console.log((010).toString(2)); // Convert for binary
// expect output: '1000'

console.log((10).toString(8)); // Convert for octal
// expect output: '12'
