console.log(Number("10"));
// expect output: 10

console.log(Number("9.9"));
// expect output: 9.9

console.log(Number("0xFF"));
// expect output: 255

console.log(Number("0b10"));
// expect output: 2

console.log(Number("0o10"));
// expect output: 8

console.log(Number());
// expect output: 0

console.log(Number("JavaScript"));
// expect output: NaN

